function SCROLL_DOWN(e) { window.scrollBy(0, 100); }
function SCROLL_UP(e) { window.scrollBy(0, -100); }
function SCROLL_TOP(e) { window.scrollTo(0, 0); }
function SCROLL_BOTTOM(e) { window.scrollTo(0, document.body.scrollHeight); }
function PAGE_DOWN(e) { window.scrollBy(0, 500); }
function PAGE_UP(e) { window.scrollBy(0, -500); }
function PAGE_HALF_DOWN(e) { window.scrollBy(0, 250); }
function PAGE_HALF_UP(e) { window.scrollBy(0, -250); }
